package cfp10;

public class UN06_EJ05_Nombres {

    public static void main(String[] args) {

        System.out.println("Array nombres");
        System.out.println("");

        String vector[] = {"Pepe", "Carlos", "Beto", "Pablo", "Marcos", "Jose", "Juan", "Daniel"};

        System.out.println("");
        for (String nombres : vector) {
            System.out.println(nombres);
        }
        System.out.println("");
    }
}
