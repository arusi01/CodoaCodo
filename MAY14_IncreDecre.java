
package cfp10;

public class MAY14_IncreDecre {
    
    public static void main(String[] args) {
        
        int numero;
        numero=0;
        
        System.out.println("El valor de numero es: "+numero);
        System.out.println(++numero);
        System.out.println("El valor de numero es: "+numero);
        System.out.println(numero++);
        System.out.println("El valor de numero es: "+numero);
        System.out.println(--numero);
        System.out.println("El valor de numero es: "+numero);
        System.out.println(numero--);
        System.out.println("El valor de numero es: "+numero);
    }
    
}
