    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String datos[]= new String[3];
        boolean verifica;
        verifica=txtNombre.getText().equals("") ||
                txtApellido.getText().equals("") ||
                txtTelefono.getText().equals("");
        if (!verifica) { // [nombre],[apellido],[telefono], FILA
            datos[0]=txtNombre.getText();
            datos[1]=txtApellido.getText();
            datos[2]=txtTelefono.getText();
            modelo.addRow(datos);
            txtNombre.setText("");
            txtApellido.setText("");
            txtTelefono.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Falta el ingreso de un dato!!");
        }
 
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila;
        fila = Tabla.getSelectedRow();
        if (fila >= 0) {
            modelo.removeRow(fila);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada;
        filaSeleccionada = Tabla.getSelectedRow();
        if (filaSeleccionada >= 0) {
           txtNombre.setText(Tabla.getValueAt(filaSeleccionada, 0).toString());
           txtApellido.setText(Tabla.getValueAt(filaSeleccionada, 1).toString());
           txtTelefono.setText(Tabla.getValueAt(filaSeleccionada, 2).toString());
           modelo.removeRow(filaSeleccionada);
        }else{
            JOptionPane.showMessageDialog(null, "Fila no Seleccionada!!");            
        }
    }//GEN-LAST:event_btnModificarActionPerformed

