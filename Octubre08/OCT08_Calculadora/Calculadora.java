    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        boolean verifica;
        int nro1,nro2,resultado;
        
        verifica=txtNro1.getText().equals("") ||
                txtNro2.getText().equals("");
        
        if (!verifica && comboOperacion.getSelectedIndex()>0) {             
            nro1=Integer.parseInt(txtNro1.getText());
            nro2=Integer.parseInt(txtNro2.getText());
            
            switch (comboOperacion.getSelectedIndex()) {
                case 1:
                    resultado=nro1+nro2;
                    txtResultado.setText(Integer.toString(resultado));
                    break;
                case 2:
                    resultado=nro1-nro2;
                    txtResultado.setText(Integer.toString(resultado));
                    break;
                case 3:
                    resultado=nro1*nro2;
                    txtResultado.setText(Integer.toString(resultado));
                    break;
                case 4:
                    if (nro2 == 0) {
                        JOptionPane.showMessageDialog(null, "No se puede Dividir");
                    } else {
                        resultado = nro1 / nro2;
                    txtResultado.setText(Integer.toString(resultado));
                    }
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falta el ingreso de un dato!!");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

